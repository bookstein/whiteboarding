# a heap is a tree where every root node is:
# parent > children
# or children > parent

# a binary tree simply has 2 children -- not ordered

# a binary heap: every node has 2 children,
# where parent is greater than children -- "Max Heap"
# where children are greater than parent -- "Min Heap"

# "max bin heap" -- every node has 2 pointers, parent node greatest
# "min bin heap" -- every node has 2 pointers, children greater

# a heap is a Priority Queue:
    # Max Heap: first value is highest value (but the rest isn't sorted)
    # Min Heap: first value is lowest value (but the rest isn't sorted)


